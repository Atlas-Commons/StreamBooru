#!/usr/bin/env node
/**
 * Sync Android versionCode/versionName from package.json or RELEASE_VERSION / GITHUB_REF_NAME.
 * Beta example: 1.1.0-beta.1 -> versionName 1.1.0-v1beta, apk streambooru-1.1.0-v1beta.apk
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');

function readPackageVersion() {
  try {
    const pkg = JSON.parse(fs.readFileSync(path.join(root, 'package.json'), 'utf8'));
    return String(pkg.version || '').trim();
  } catch {
    return '';
  }
}

function resolveVersion() {
  const fromEnv = String(process.env.RELEASE_VERSION || '').trim();
  if (fromEnv) return fromEnv.replace(/^v/, '');
  const refName = String(process.env.GITHUB_REF_NAME || '').trim();
  if (refName && /^v?\d+\.\d+\.\d+/.test(refName)) return refName.replace(/^v/, '');
  return readPackageVersion();
}

/** @param {string} version */
export function parseAndroidVersion(version) {
  const raw = String(version || '').trim();
  const m = raw.match(/^(\d+)\.(\d+)\.(\d+)(?:-beta\.(\d+))?(?:[-+].*)?$/i);
  if (!m) throw new Error(`Unsupported version format: ${raw}`);

  const major = Number(m[1]);
  const minor = Number(m[2]);
  const patch = Number(m[3]);
  const betaNum = m[4] ? Number(m[4]) : 0;

  const base = major * 1_000_000 + minor * 10_000 + patch * 100;
  const versionCode = base + (betaNum > 0 ? betaNum : 100);
  const baseName = `${major}.${minor}.${patch}`;

  if (betaNum > 0) {
    const versionName = `${baseName}-v${betaNum}beta`;
    const apkBasename = `streambooru-${versionName}`;
    return { version, versionCode, versionName, apkBasename, isBeta: true };
  }

  const versionName = baseName;
  const apkBasename = 'streambooru';
  return { version, versionCode, versionName, apkBasename, isBeta: false };
}

function main() {
  const version = resolveVersion();
  if (!version) {
    console.error('sync-android-version: no version found');
    process.exit(1);
  }

  const info = parseAndroidVersion(version);
  const props = [
    `# Generated by scripts/sync-android-version.mjs — do not edit`,
    `VERSION_CODE=${info.versionCode}`,
    `VERSION_NAME=${info.versionName}`,
    `APK_BASENAME=${info.apkBasename}`,
    `IS_BETA=${info.isBeta ? 'true' : 'false'}`,
    ''
  ].join('\n');

  const outPath = path.join(root, 'android', 'version.properties');
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, props, 'utf8');

  console.log(`Android version: code=${info.versionCode} name=${info.versionName} apk=${info.apkBasename}.apk`);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  main();
}
